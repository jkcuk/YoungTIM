package javawaveoptics.geometry;

public class Point2D
{
	double i;
	double j;
	
	public Point2D(double i, double j)
	{
		this.i = i;
		this.j = j;
	}
	
	public Point2D(Point2D point)
	{
		this(point.getI(), point.getJ());
	}
	
	public Point2D()
	{
		this(0, 0);
	}
	
	public double getISquared()
	{
		return i * i;
	}
	
	public double getJSquared()
	{
		return j * j;
	}

	public double getI()
	{
		return i;
	}

	public void setI(double i)
	{
		this.i = i;
	}

	public double getJ()
	{
		return j;
	}

	public void setJ(double j)
	{
		this.j = j;
	}
}
