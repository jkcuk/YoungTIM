package javawaveoptics.ui;

import java.util.EventListener;

/**
 * Listener for when edits are made to an optical component, such as changing a parameter or swapping
 * inputs or outputs.
 * 
 * @author Sean
 */
public interface OpticalComponentEditListener extends EventListener
{
	/**
	 * Notifies the class implementing this listener that an edit has been made and an appropriate
	 * action should be taken.
	 */
	public void editMade();
	
	/**
	 * Notifies the class implementing this listener that something has happened which should be redrawn.
	 */
	public void redraw();
}
